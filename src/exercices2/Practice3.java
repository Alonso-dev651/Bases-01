package exercices2;

public class Practice3 {
    public static void main(String[] args) {
        System.out.println("Numero del 10 al 1 - Descendente");

        for (int i = 10; i >= 1; i--) {
            if (i == 1) System.out.print(i);
            else System.out.print(i + " ");
        }
    }
}
